/**
 * 默认分隔符
 */
export const DEFAULT_SEPARATOR = '/';

/**
 * 检测有意义的前缀的正则模式（仅用于默认分隔符 '/'）
 * 顺序很重要：更具体的模式要放在前面
 */
export const PRESERVED_PREFIXES = [
  /^\.\.\//, // ../
  /^\.\//, // ./
  /^~\//, // ~/
  /^[a-zA-Z][a-zA-Z0-9+.-]*:\/\/\/?/, // 协议:// 或 协议:/// (http://, https://, file:///, etc.)
  /^\/\/(?!\/)/, // // 后面紧跟非斜杠字符 (协议相对 URL)
  /^\//, // / (绝对路径)
];
